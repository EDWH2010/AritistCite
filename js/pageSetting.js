//Slide-FadeEffectImages
let imgIndex=0;

function imageUpdate(slider,timeout){
    
  let imgs = slider.children();
   if(++imgIndex >= imgs.size())
     imgIndex=0;
    imgs.fadeOut(timeout);

  imgs.each(function(index,element){   
        var $e = $(element);
        if(index == imgIndex){
            $e.fadeIn(timeout);
        }
  });
}

function imageChange(slider,index,timeout){
    if(imgIndex == index){
        return;
    }
    
    let imgs = slider.children();
    imgs.fadeOut(timeout);

    imgIndex=index;

     imgs.each(function(i,element){   
        if(i == imgIndex){
            $(element).fadeIn(timeout);
        }
  });
}


